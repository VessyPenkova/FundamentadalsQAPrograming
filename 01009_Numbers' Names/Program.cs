namespace _01009_Numbers__Names
{
    internal class Program
    {
        static void Main(string[] args)
        {
            int inputNumberToCheck = int.Parse(Console.ReadLine());
            if (inputNumberToCheck == 1)
            {
                Console.WriteLine("one");
            }
            else if (inputNumberToCheck == 2)
            {
                Console.WriteLine("two");
            }
            else if (inputNumberToCheck == 3)
            {
                Console.WriteLine("three");
            }
            else if (inputNumberToCheck == 4)
            {
                Console.WriteLine("four");
            }
            else if (inputNumberToCheck == 5)
            {
                Console.WriteLine("five");
            }
            else if (inputNumberToCheck == 6)
            {
                Console.WriteLine("six");
            }
            else if (inputNumberToCheck == 7)
            {
                Console.WriteLine("seven");
            }
            else if (inputNumberToCheck == 8)
            {
                Console.WriteLine("eight");
            }
            else if (inputNumberToCheck == 9)
            {
                Console.WriteLine("nine");
            }
            else if (inputNumberToCheck == 10)
            {
                Console.WriteLine("ten");
            }
            else
            {
                Console.WriteLine("number out of range");
            }
        }
    }
}